    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        
        // Taking the username and password that are written to empty area
        String username = TextField_username.getText();
        String password = String.valueOf(TextField_password.getPassword());
        
        // To find the user by its username and password, a query will be performed on database
        String query = "SELECT * FROM user_details WHERE username = ? AND password = ?";
        
        try {
            ps = My_Database.getConnection().prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);
            rs = ps.executeQuery();
            
            if(rs.next()) {
                // If user is authenticated, open home page and close the login page
                Home_Page home = new Home_Page();
                home.setVisible(true);
                home.pack();
                home.setLocationRelativeTo(null);
                this.dispose();
            }
            else {
                // If user is NOT authenticated, give an error message
                JOptionPane.showMessageDialog(null, "Invalid username or password was given!", "Login Error", 2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_LoginActionPerformed

